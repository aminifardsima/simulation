function dPhiincident(xp2,yp2,nx2,ny2)
use var
implicit none
!****************************************************************************************************************************
 !moje takht
 !**********************************************************************************************************************
!variables
  complex(8)::		 dPhiincident,PHIinc
  real(8)::			xp2,yp2,nx2,ny2,kr,kn
!****************************************************************************************************************************
    kr=kax*xp2+kay*yp2																		!Kvecta.r
    kn=kax*nx2+kay*ny2																		!Kvecta.n_hat
   
    PHIinc=E0*CDExp(IMG*kr)																				!moje forudi
    dPhiincident=IMG*kn*PHIinc
!--------------------------------------------------------------------------------------------------------------------------------------------------------
end function dPhiincident

