function phiIncident(xp2,yp2)
use var
use imsl
implicit none
!****************************************************************************************************************************
!variables
 complex(8)::		phiIncident
 real(8)::			xp2,yp2,kr

!****************************************************************************************************************************
 kr=kax*xp2+kay*yp2																		!Kvecta.r
 phiIncident=E0*CDExp(IMG*kr)	
!--------------------------------------------------------------------------------------------------------------------------------------------------------
end function phiIncident

